//Incrimento e decremento com valor especifico
//operadores de atribuição aritimpetica
var x = 10;
var y = 5;

console.log("------------------------")
console.log("x =", x, "| y =, y");

x += y; //x = x + y
console.log("x =", x, "| y =", y);

x -= y; //x = x - y
console.log("x =", x, "| y =", y);

x /= y; //x = x / y
console.log("x =", x, "| y =", y);

x **= y; //x = x ** y
console.log("x =", x, "| y =", y);

x %= y; //x = x % y
console.log("x =", x, "| y =", y);

x = 13;
x %= y; //x = x % y
console.log("x =", x, "| y =", y);